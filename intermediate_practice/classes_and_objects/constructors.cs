A class constructor is a special member method of a class that is executed whenever a new object of that class is created.

A constructor has exactly the same name as its class, is public, and does not have any return type.

The constructor is like a built-in method of a class that gets called when you create a new instance of the class. A class is like a template, and then you call the constructor to create an instance of the template.
For example, if you have a Person class or template, it might come with data holders like hair_color, favorite_color, and height. The you use the constructor method to create an instance of the Person class called Meagan that has hair_color: brown, favorite_color: green, and height: 64".
